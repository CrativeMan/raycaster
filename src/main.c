void drawDebug() {
  DrawRectangle(0, 0, 200, 100, RAYWHITE);
  DrawFPS(10, 10);
  DrawText(TextFormat("player:\n\nx:%.1f | y:%.1f", player.pos.x, player.pos.y),
           10, 30, 25, DARKGREEN);
}

void gameLoop() {
  for (int y = 0; y < MAPHEIGHT; y++) {
    for (int x = 0; x < MAPWIDTH; x++) {
      Color color;
      switch (map[y][x]) {
      case 1:
        color = RAYWHITE;
        break;
      case 2:
        color = RED;
        break;
      case 3:
        color = GREEN;
        break;
      }
      ClearBackground(color);
    }
  }
}

void initWindow() {
  int monitor = GetCurrentMonitor();
  scrInfo.width = GetMonitorWidth(monitor);
  scrInfo.height = GetMonitorHeight(monitor);

  InitWindow(scrInfo.width, scrInfo.height, "raycaster");

  ToggleBorderlessWindowed();

  SetTargetFPS(30);
}

int main(int argc, char **argv) {
  initWindow();

  // debug stuff
  bool debug = false;
  if (argc == 2)
    if (strcmp(argv[1], " -d"))
      debug = true;

  player.pos.x = 5;
  player.pos.y = 20;
  player.dir.x = 0;
  player.dir.y = 0;

  while (!WindowShouldClose()) {
    BeginDrawing();

    ClearBackground(BLACK);

    gameLoop();

    if (debug == true) {
      drawDebug();
    }

    EndDrawing();
  }
  CloseWindow();

  return 0;
}
